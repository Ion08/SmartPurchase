export function translate(language: Language, key: string) {
  if (language === 'ro') {
    return RO[key] ?? EN[key] ?? key;
  }
  return EN[key] ?? key;
}

export function useI18n() {
  const language = useAppStore((state) => state.profile.language ?? 'en');
  return {
    language,
    t: (key: string) => translate(language, key)
  };
}
